namespace Chi.Lexing
{
    /// <summary>
    /// Lists the punctuation symbols used in the language.
    /// </summary>
    public static class Punctuations
    {
        public const char Hash = '#';
        public const char BraceOpen = '{';
        public const char BraceClose = '}';
        public const char ParenOpen = '(';
        public const char ParenClose = ')';
        public const char BrackOpen = '[';
        public const char BrackClose = ']';
        public const char AngularOpen = '<';
        public const char AngularClose = '>';
        public const char Dot = '.';
        public const char Colon = ':';
        public const char Comma = ',';
        public const char Semicolon = ';';
        public const char Equal = '=';
        public const char Underscore = '_';
        public const char Dollar = '$';
    }
}
